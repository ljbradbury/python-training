first_name = 'lyndon'
last_name = 'bradbury'

full_name = first_name + ' ' + last_name

print(full_name)    # Prints the string in the same case
print(full_name.title())    # Prints the string with capital first letters
print(full_name.upper())    # Prints the string all upper case
print(full_name.lower())    # Prints the string all lower case

